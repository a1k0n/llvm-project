#ifndef LLVM_LIB_TARGET_MCS51_INSTPRINTER_MCS51INSTPRINTER_H
#define LLVM_LIB_TARGET_MCS51_INSTPRINTER_MCS51INSTPRINTER_H

#include "MCTargetDesc/MCS51MCTargetDesc.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class MCOperand;

class MCS51InstPrinter : public MCInstPrinter {
public:
  MCS51InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                   const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}
  
  virtual ~MCS51InstPrinter();

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                         const MCSubtargetInfo &STI, raw_ostream &OS) override;

  void printRegName(raw_ostream &O, unsigned RegNo) const override;

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);

  void printPCRelImm8(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printAbsAddr16(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printDirectA8(const MCInst *MI, unsigned OpNo, raw_ostream &O);

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
};
}

#endif

